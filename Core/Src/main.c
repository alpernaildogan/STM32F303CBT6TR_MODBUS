void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {

	/* This function is called when the respective timer overflows
	 * it is used for 100ms timer, i.e. TIM3
	 * When the 100 ms passed the count value on the TIM2, i.e. pulse counter, saved in to the first Holding Register
	 * See below for further details.
	 * */


	/********************** Pulse Counter **********************/
	if (htim->Instance == TIM3) {

		Counts = TIM2->CNT;

		if (Counts <= 65535) { //the biggest number that can be sent with single 16-bit holding register

			// note that first element of holding register is for counts
			Update_Holding_Register_value(Counts, 0);

		} else {
			// to prevent overflow, i.e. the highest possible count can't be exceeded
			Update_Holding_Register_value(65535, 0);
		}

		TIM3->CNT = 0; //Reset the timers
		TIM2->CNT = 0;
	}
}

